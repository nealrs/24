function startTimer(duration, action){
	const timer = document.getElementById(`timer`);
	let tl = duration;

	const downloadTimer = setInterval(() => {
		if (tl < 10){
		  timer.style.color = '#FF4136';
		}
		else if (tl < 30){
		  timer.style.color = '#FFDC00';
		}
		else {
		  timer.style.color = '#3D9970';
		}

		if(tl <= 0){
		  clearInterval(downloadTimer);
		  timer.innerHTML = "0";

		  if (action == true){
			answer();
		  } else {
			newGame();
		  }

		} else {
		    timer.innerHTML = tl;
		}
		tl--;
	}, 1000);
}
